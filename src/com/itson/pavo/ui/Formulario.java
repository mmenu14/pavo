    private void listaEstados(String estado, Persona p){
        try {
            Map<String, String> map = new HashMap<>();
            map.put("AS", "Aguascalientes");		map.put("CS", "Chiapas");
            map.put("BC", "Baja California");		map.put("CH", "Chihuahua");
            map.put("BS", "Baja California Sur");	map.put("DF", "Distrito Federal");
            map.put("CC", "Campeche");                  map.put("DG", "Durango");
            map.put("CL", "Coahuila de Zaragoza");	map.put("GT", "Guanajuato");
            map.put("CM", "Colima");                    map.put("GR", "Guerrero");
            map.put("HG", "Hidalgo");                   map.put("JC", "Jalisco");
            map.put("MC", "México");                    map.put("MN", "Michoacán de Ocampo");
            map.put("MS", "Morelos");                   map.put("NT", "Nayarit");
            map.put("NL", "Nuevo León");                map.put("OC", "Oaxaca");
            map.put("PL", "Puebla");                    map.put("QT", "Querétaro");
            map.put("QR", "Quintana Roo");              map.put("SP", "San Luis Potosí");
            map.put("SL", "Sinaloa");                   map.put("SR", "Sonora");
            map.put("TC", "Tabasco");                   map.put("TS", "Tamaulipas");
            map.put("TL", "Tlaxcala");                  map.put("VZ", "Veracruz de Ignacio de la Llave");
            map.put("YN", "Yucatán");                   map.put("ZS", "Zacatecas");
            map.put("NE", "Nacido en el Extranjero");
            map.forEach((k, v)->{
                if(v.equals(estado)){
                    p.setEstadoCodigo(k);
                }
            });
        } catch (Exception e) {
            System.out.println("error: "+e.getMessage());
        }

    }
