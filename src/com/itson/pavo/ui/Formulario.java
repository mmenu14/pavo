    private void btnGenerarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGenerarMouseClicked
        // TODO add your handling code here:
        try {
            Persona persona = new Persona();

            persona.setNombre(txtNombre.getText());
            persona.setApellido(txtApellido.getText());
            persona.setDia(txtDia.getText());
            persona.setAño(txtAño.getText());
            persona.setEstado(jEstado.getSelectedItem().toString());
            persona.setMes(txtMes.getText());
            persona.setGenero(jbSexo.getSelectedItem().toString());
            if(
                    !persona.getNombre().equals("") &&
                    !persona.getApellido().equals("") &&
                    !persona.getDia().equals("") &&
                    !persona.getApellido().equals("") &&
                    !persona.getEstado().equals("") &&
                    !persona.getMes().equals("") &&
                    !persona.getGenero().equals("")
            ){
                String curp ="";
            

    //        Primera letra del primer apillido 
            curp = String.valueOf(persona.getApellido().charAt(0));
    //        primera vocal interna del primer apellido
            char[] apellidoArr = persona.getApellido().toCharArray();
            System.out.println(apellidoArr[1]);
            for(int i = 0; i < apellidoArr.length; i++ ){
                if("aeiou".indexOf(apellidoArr[i]) != -1 && "aeiou".indexOf(apellidoArr[i]) != 0){
                    curp += apellidoArr[i];
                    break;
                }
            }
    //        Primera letra del segundo apellido. En caso de no tener segundo apellido, se colocará X.
            if(persona.getApellido().split(" ").length > 1){
                curp += persona.getApellido().split(" ")[1].charAt(0);
            }else{
                curp += "X";
            }

    //        Primera letra del nombre de pila. En nombres compuestos que comiencen con José o María, 
    //        se tomará en cuenta la primera letra segundo nombre
            if(persona.getNombre().split(" ").length > 1){
                curp += persona.getNombre().split(" ")[1].charAt(0);
            }else{
                curp += persona.getNombre().charAt(0);
            }

            curp += persona.getAño().substring(persona.getAño().length() - 2, persona.getAño().length())+""+persona.getMes()+""+persona.getDia();

            curp += persona.getGenero().charAt(0);
    //        Dos letras correspondientes a la entidad de nacimiento. Consultar las claves aquí.
              listaEstados(persona.getEstado(), persona);
              curp += persona.getEstadoCodigo();

    //         Primera constante interna (no inicial) del primer apellido
            for(int i = 1; i < apellidoArr.length; i++ ){
                if("aeiou".indexOf(apellidoArr[i]) == -1){
                    curp += apellidoArr[i];
                    break;
                }
            }

    //        Primera consonante interna (no inicial) del segundo apellido. En caso de no tener segundo apellido, se colocará X.
            if(persona.getApellido().split(" ").length == 1){
                curp += "X";
            }else{
             String segundoApellido = persona.getApellido().split(" ")[1];
                char[] segundoApellidoArr = segundoApellido.toCharArray();
                for(int i = 1; i < segundoApellidoArr.length; i++ ){
                    if("aeiou".indexOf(segundoApellidoArr[i]) == -1 && i > 0){
                        curp += segundoApellidoArr[i];
                        break;
                    }
                }   
            }
//            Primera consonante interna (no inicial) del nombre
            char[] nombre = persona.getNombre().toCharArray();

            for(int i = 1; i < nombre.length; i++ ){
                    if("aeiou".indexOf(nombre[i]) == -1 && i > 0){
                        curp += nombre[i];
                        break;
                    }
                }

            if(Integer.parseInt(persona.getAño()) <= 1999){
                curp += "0";
            }else{
                curp += "A";
            }

            curp = curp.toUpperCase();
           txtResultado.setText(txtResultado.getText()+curp);
            }else{
                JOptionPane.showMessageDialog(null, "Por favor llene todos los campos");
            }
        } catch (Exception e) {
            System.out.println("Error: "+e.getMessage());
        }
    }//GEN-LAST:event_btnGenerarMouseClicked
    
